<template>
    <div class="home">
        <HelloWorld msg="Welcome to Your Vue.js App" />
        <button @click="logout">logout</button>
    </div>
</template>

<script>
// @ is an alias to /src
import HelloWorld from "@/components/HelloWorld.vue";
import firebase from "firebase";

export default {
    name: "home",
    components: {
        HelloWorld
    },
    methods: {
        logout: function() {
            firebase
                .auth()
                .signOut()
                .then(() => {
                    this.$router.replace("login");
                });
        }
    }
};
</script>
